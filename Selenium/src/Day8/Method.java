public class Method {
	
	
	// No params no return  
	void m1() {
		System.out.println("Im no parameter no argumet method");
	}
	
	// no params return value 
	String m2() {
		return("Return Method");
	}
	
	//no return  take params 
	void m3(String name)
	{
		System.out.println(name);
	}

	// Takes params and return value 
	int m4(int id)
	{
		return 8;
	}
}
